#include<bits/stdtr1c++.h>
using namespace std;
int main(int argc, char const *argv[])
{
    cout<<"for loop"<<endl;
    for (int i = 1; i <= 10; i++)
    {
        cout<<i<<endl;
    }
    cout<<endl;
    

    cout<<"while loop"<<endl;
    int n =1;
    while (n <=10)
    {
        cout<<n<<endl;
        n++;
    }
    cout<<endl;
    

    cout<<"do-while loop"<<endl;
    int i = 1;
    do
    {
        cout<<i<<endl;
        i++;
    } while (i<=10);
    cout<<endl;
    


    
    

    return 0;
}
